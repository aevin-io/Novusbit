 <h2>What's a novus?</h2>
<p>Novusbit relies on a simple idea. Each member starts a story; call it a Novus. Other members continue them by writing Bits of text. Bit by bit the story gets a new twist!</p>


<p>Each novus is a collaborative effort of all its authors who have contribute to the story. </p>

<p>A novus can be a fictional story, a fairy tail, a poem or even an essay collaboratively written by experts.</p>

<p>In case you want a geeky way to describe it, is a collaborative writing platform. The difference is that author may not edit each others bits. In novusbit our mainly goal is to create a vast collection of interesting and intriguing stories written by many. Authors here, do not merely write the perfect novel, expecting appraising comments and "likes". Authors publishing their novuses or bits with the thrill to see their inspiration grow and develop by others further.</p>

<p>Novusbit is all about openness and collaboration. Everyone is welcome.</p>


<p>Novus is published - A seed 
Bits extend novus - The leaves.</p>
