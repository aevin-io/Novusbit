<h2>Novus Types</h2> 
<p>Novusbit is also about fun and entertaining.
How can writing be fun, especially for the non-exprerts. The answer is found in every game.
Challenges.</p>

<p>There are 7 types of novus, all very interesting; really.</p>
<p>
<ul>
<li><strong>Classic novus.</strong> This is the type where no artificial challenges or limitations apply. The only think you have to do is to write an interesing and inspired bit.</li>
<li><strong>Hourglass.</strong> When the clock is ticking, things are getting more exciting. Write your bit in 2 minutes!</li>
<li><strong>Laconism.</strong> Sometimes saying less has more meaning. Write your bit but have in mind the word limit!</li>
<li><strong>Short Sight.</strong> A novus might have 100 bits. You just get to see the last one. Can you write a bit that fits well with all the rest, judging just by the last one? The full story is presented to you when you post your bit. The results may be hilarious! </li>
<li><strong>Cloud nine.</strong> Do you know what's a keyword or tag cloud? The 6 most frequent keywords of all bits are presented to you. Can you write your bit and use all of them? (Cloud nine is an experimental approach to keep the integrity of what's written)</li>
<li><strong>Prose Worm.</strong> So simple, yet challenging: Your bit must start with the last word .. of the last bit. Do you take the challenge? (Nope, you cant delete the first word of your bit, seriously, you just can't).</li>
<li><strong>Irriversable.</strong> Seemed like the novus types weren't enough weird and then we thought of that. How about instead of appending each bit at the end of the story...Each bit will be placed on top of all other? There you have it, a reversed story! The first bit entered is the end of the story, the others is a backwards journey to find where all started!</li>
</ul>
</p>



